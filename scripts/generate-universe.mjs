// Generates src/lib/universe-data.ts from official NSE source files:
//   data/EQUITY_L.csv  — NSE's full list of listed equities (nsearchives.nseindia.com/content/equities/EQUITY_L.csv)
//   data/nifty500.csv  — Nifty 500 constituents with industries (niftyindices.com/IndexConstituent/ind_nifty500list.csv)
// Curated display names/sectors/abouts for well-known stocks come from src/lib/curated-info.ts.
//
// Run: node scripts/generate-universe.mjs
// Refresh the CSVs first if you want the latest listings.

import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");

function parseCsv(text) {
  const lines = text.trim().split(/\r?\n/);
  const headers = splitCsvLine(lines[0]).map((h) => h.trim());
  return lines.slice(1).map((line) => {
    const cells = splitCsvLine(line);
    const row = {};
    headers.forEach((h, i) => (row[h] = (cells[i] ?? "").trim()));
    return row;
  });
}

// Handles quoted cells containing commas (company names like "ABC, Ltd").
function splitCsvLine(line) {
  const out = [];
  let cur = "";
  let inQ = false;
  for (const ch of line) {
    if (ch === '"') inQ = !inQ;
    else if (ch === "," && !inQ) {
      out.push(cur);
      cur = "";
    } else cur += ch;
  }
  out.push(cur);
  return out;
}

// NSE Nifty-500 industry vocabulary -> the app's shorter sector labels.
const INDUSTRY_MAP = {
  "Financial Services": "Finance",
  "Information Technology": "IT",
  "Oil Gas & Consumable Fuels": "Energy",
  "Fast Moving Consumer Goods": "FMCG",
  "Automobile and Auto Components": "Auto",
  "Metals & Mining": "Metals",
  Construction: "Infra",
  "Construction Materials": "Cement",
  Power: "Power",
  Telecommunication: "Telecom",
  "Consumer Services": "Consumer",
  "Consumer Durables": "Consumer",
  "Capital Goods": "Industrials",
  Chemicals: "Chemicals",
  Textiles: "Textiles",
  "Media Entertainment & Publication": "Media",
  Realty: "Realty",
  Healthcare: "Healthcare",
  Services: "Services",
  Diversified: "Diversified",
  "Forest Materials": "Materials",
};

function tidyName(name) {
  return name
    .replace(/\s+Limited$/i, "")
    .replace(/\s+Ltd\.?$/i, "")
    .trim();
}

const equities = parseCsv(readFileSync(join(root, "data/EQUITY_L.csv"), "utf8"));
const n500 = parseCsv(readFileSync(join(root, "data/nifty500.csv"), "utf8"));

// Curated info (names/sectors/abouts) — kept as a TS module the app also imports.
const curatedSrc = readFileSync(join(root, "src/lib/curated-info.ts"), "utf8");
const curated = new Map();
const entryRe = /"([A-Z0-9&-]+)":\s*\{\s*sector:\s*"([^"]*)"/g;
let m;
while ((m = entryRe.exec(curatedSrc))) curated.set(m[1], { sector: m[2] });

const industryBySymbol = new Map(
  n500.map((r) => [r.Symbol, INDUSTRY_MAP[r.Industry] ?? r.Industry]),
);

const rows = [];
const seen = new Set();
for (const r of equities) {
  const series = r.SERIES ?? r[" SERIES"];
  // EQ = normal rolling settlement, BE = trade-for-trade. BZ (surveillance/
  // suspended listings) is excluded — most have no tradable market data.
  if (series !== "EQ" && series !== "BE") continue;
  const symbol = r.SYMBOL;
  if (!symbol || seen.has(symbol)) continue;
  seen.add(symbol);
  const sector = curated.get(symbol)?.sector ?? industryBySymbol.get(symbol) ?? "Other";
  rows.push([
    symbol,
    tidyName(r["NAME OF COMPANY"]),
    sector,
    r["ISIN NUMBER"] ?? r[" ISIN NUMBER"],
    r["DATE OF LISTING"] ?? r[" DATE OF LISTING"],
    Number(r["FACE VALUE"] ?? r[" FACE VALUE"]) || 0,
  ]);
}

// Curated symbols that aren't NSE equities (commodity ETFs etc.) stay in the
// universe — they're real NSE-traded instruments, just not in EQUITY_L.
for (const [symbol, info] of curated) {
  if (seen.has(symbol)) continue;
  rows.push([symbol, symbol, info.sector, "", "", 0]);
}

rows.sort((a, b) => a[0].localeCompare(b[0]));

const header = `// AUTO-GENERATED by scripts/generate-universe.mjs — do not edit by hand.
// Source: NSE EQUITY_L.csv (full listed-equity list) + Nifty 500 industry map.
// [symbol, name, sector, isin, listedOn, faceValue]
export type UniverseRow = [string, string, string, string, string, number];
export const UNIVERSE_ROWS: UniverseRow[] = [
`;
const body = rows
  .map((r) => `  ${JSON.stringify(r)},`)
  .join("\n");
writeFileSync(join(root, "src/lib/universe-data.ts"), header + body + "\n];\n");
console.log(`Wrote ${rows.length} rows to src/lib/universe-data.ts`);
